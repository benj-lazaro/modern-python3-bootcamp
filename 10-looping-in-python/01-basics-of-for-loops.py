# Basic syntax of a for loop
print("Print individual characters from a string:")
for character_string in "coffee":
    print(character_string)
    # Print individual letters 10x
    print(character_string * 10)

# for loop with ranges
print("Print individual numbers using range():")
for number_value in range (1, 10):
    print(number_value)
    # Square the values
    print(number_value * number_value)

