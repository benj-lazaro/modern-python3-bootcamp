# Variables

# Variable assignment
x = 100
khaleesi_mother_of_dragons = 1
print(khaleesi_mother_of_dragons + x)

# Variable assignment to another
current_cash = 100
my_wallet = current_cash
print(current_cash)
print(my_wallet)

# Variable value re-assignment
current_value = 12
print(current_value)

current_value = "It's me a Mario!"
print(current_value)

current_value = True
print(current_value)

# Multiple variables assignment
user1, user2, user3 = "george", "michael", "jackson"
print(user1)
print(user2)
print(user3)
