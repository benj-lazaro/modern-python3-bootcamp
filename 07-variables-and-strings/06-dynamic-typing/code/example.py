# Dynamic Typing

# Initially assigned the variable with a boolean value
awesomeness = True
print(awesomeness)

# Then to a string
awesomeness = "a dog"
print(awesomeness)

# Then to a none (similar to JavaScript's null)
awesomeness = None
print(awesomeness)

# Lastly to a float
awesomeness = 22 / 7
print(awesomeness)
