# Data Types

# bool
is_true = True
is_game_over = False
print(is_true)
print(type(is_true))
print(is_game_over)
print(type(is_game_over))

# str
some_string = "This is a string data type"
print(some_string)
print(type(some_string))

# list
numbers = [1, 2, 3]
print(numbers)
print(type(numbers))

# dict
gamer_bio = {"login_name": "playerOne", "first_name": "Thomas", "last_name": "Anderson"}
print(gamer_bio)
print(type(gamer_bio))