# Commonly used math operators
# Addition
print("Addition")
print(1 + 3)

# Subtraction
print("Subtraction")
print(40 - 4.5)

# Multiplication
print("Multiplication")
print(5 * 8)

# Division -> always yields a float result
print("Division")
print(1/2)
print(1/3)

# Order of operations -> PEMDAS (Parenthesis Exponents Multiplication Division Addition Subtraction)
print("PEMDAS")
print(1 + 2 * 5 / 3)
print((1 + 2) * 5 / 3)
