# Dynamic typing demonstration

awesomeness = True          # A boolean data type
print(type(awesomeness))
print(awesomeness)

awesomeness = "a dog"       # Reassigned a string data type
print(type(awesomeness))
print(awesomeness)

awesomeness = None          # Reassigned a None data type
print(type(awesomeness))
print(awesomeness)

awesomeness = 22            # Reassigned an integer data type
print(type(awesomeness))
print(awesomeness)
