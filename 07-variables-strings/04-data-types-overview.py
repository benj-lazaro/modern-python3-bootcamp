# Boolean data type
logged_in = True
print(type(logged_in))
print(logged_in)

game_over = False
print(type(game_over))
print(game_over)

# Integer data type
number_of_coffee = 2
print(type(number_of_coffee))
print(number_of_coffee)

# String data type
gamer_name = "Amadeus"
print(type(gamer_name))
print(gamer_name)

some_string = "8"
print(type(some_string))
print(some_string)

some_string = "Hello! I am some string!"
print(type(some_string))
print(some_string)


# List data type
list_of_numbers = [1, 2, 3]
print(type(list_of_numbers))
print(list_of_numbers)

# Dictionary data type
user_account = {"first_name": "Colt", "last_name": "Steele"}
print(type(user_account))
print(user_account)
