# String Escape Sequence Demo

# ASCII Linefeed
print("Escape sequence \\n")
new_line = "Hi\nthere."
print(new_line + "\n")

# ASCII Backslash
print("Escape sequence \\")
string_line = "This is a backslash \\"
print(string_line + "\n")

# ASCII Backspace
print("Escape sequence \\b")
string_line = "Hel\blo"
print(string_line + "\n")

# ASCII Double quotes
print("Escape sequence \"")
string_line = "She said \"ha ha ha\""
print(string_line + "\n")