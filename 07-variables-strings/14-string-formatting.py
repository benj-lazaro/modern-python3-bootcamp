# Interpolate strings using f-string
number = 10
formatted_string = f"I've told you {number} times already!"
print(formatted_string)

guess_value = 8
number_guess = f"Yuur guess of {guess_value + 10} is incorrect!"
print(number_guess)


# Interpolate string using .format() method
formatted_string = "I've told you {} times already!".format(45)
print(formatted_string)
