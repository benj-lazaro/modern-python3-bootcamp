# Comparison operators demo

a = 1
b = 1
print(f"variables a = {a} & b = {b}")

expression = a == b
print(f"a == b: {expression}")

expression = a != b
print(f"a != b: {expression}")

expression = a > b
print(f"a > b: {expression}")

expression = a < b
print(f"a < b: {expression}")

expression = a >= b
print(f"a >= b: {expression}")

expression = a <= b
print(f"a <= b: {expression}")
