# Identify & print out the data type of a given number
print(type(9))      # Returns as "int"
print(type(0.1))    # Returns as "float"

# Demonstrates that Python interpreter is NOT a glorified calculator
print(1 + 4 - 2)    # Returns 3

# An int working with a float ALWAYS evaluates to float
print(1 + 1.0)      # Returns 2.0
print(20 + 0.777)   # Returns 20.777
