# Using type() function to identify the type of a number
print(type(9))  # Returns <class 'int'>
print(type(0.1)) # Return <class 'float'>

# Demonstrates that Python interpreter is NOT a glorified calculator
print(1 + 4 - 2)    # Returns 3

# Mathematical operation between an Int and a Float
print(1 + 1.0)      # Returns 2.0
print(20 + 0.777)   # Returns 20.777
