 # Python's commonly used Math Operators
# Addition
print(1 + 3)

# Subtraction
print(40 - 4.5)

# Multiplication
print(5 * 8)

# Division (NOTE: ALWAYS yields a float)
print(1/2)
print(1/3)

# PEMDAS (order of operations)
print("PEMDAS")
print(1 + 2 * 5 / 3) # Evaluates to 4.333333333333334
print((1 + 2) * 5 / 3) # Evaluates to 5.0
